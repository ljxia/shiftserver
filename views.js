// =============================================================================
// Groups
// =============================================================================

// all
// -----------------------------------------------------------------------------
function(doc)
{
  var type = doc.type;
  var groups = doc.groups;
  if(type == "user" && groups.length > 0)
  {
    for(var i = 0; i < groups.length; i++)
    {
      emit(groups[i], doc);
    }
  }
}

// =============================================================================
// User
// =============================================================================

// byname
// -----------------------------------------------------------------------------
function(doc)
{
  if(doc.type == "user")
  {
    emit(doc.userName, doc);
  }
}

// =============================================================================
// Shifts
// =============================================================================

// byuser
// -----------------------------------------------------------------------------
function(doc)
{
  if(doc.type == "shift")
  {
    emit(doc.createdBy, doc);
  }
}